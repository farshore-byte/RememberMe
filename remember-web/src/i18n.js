export const getStoredLanguage = () => {
  return localStorage.getItem('language') || 'zh';
};

export const setStoredLanguage = (lang) => {
  localStorage.setItem('language', lang);
};

// è·å–å½“å‰è¯­è¨€
export const getCurrentLanguage = () => {
  return getStoredLanguage();
};

// è·å–ç¿»è¯‘æ–‡æœ¬
